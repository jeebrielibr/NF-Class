angka1 = 25
angka2 = 2

#cara penjumlahan

hasil = angka1 + angka2
print(f"{angka1} + {angka2} = {hasil}")

#Pengurangan
hasil = angka1 - angka2
print(f"{angka1} - {angka2} = {hasil}")

#perkalian
hasil = angka1 * angka2
print(f"{angka1} X {angka2} = {hasil}")

#Pembagian 
hasil = angka1 / angka2
print(f"{angka1} / {angka2} = {hasil}")

#Modulus
hasil = angka1 % angka2
print(hasil)

#Pangkat
hasil = angka1 ** angka2
print(hasil)

#Floor Division
hasil = angka1 // angka2
print(hasil)

angka1 = angka2
print(angka1)
